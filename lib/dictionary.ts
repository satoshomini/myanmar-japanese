// 最長一致で単語を検索
export function lookupWord(text: string, startIndex: number): { word: string; meaning: string; length: number } | null {
  // 最大10文字まで試みる（長い方を優先）
  for (let len = Math.min(10, text.length - startIndex); len >= 1; len--) {
    const candidate = text.slice(startIndex, startIndex + len);
    if (dictionary[candidate]) {
      return { word: candidate, meaning: dictionary[candidate], length: len };
    }
  }
  return null;
}
